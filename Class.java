public class Calculator {
    
    public void add(double x, double y)
    {
        double result = x + y; 
        System.out.println(result);
        
    }
    
    public void subtract(double x, double y)
    {
        double result = x - y; 
        System.out.println(result);
        
    }
    public void multiply(double x, double y)
    {
        double result = x*y;
        System.out.println(result);
    }
    
    public void divide(double x, double y)
    {
        double result = x/y;
        System.out.println(result);
    }
    

}
